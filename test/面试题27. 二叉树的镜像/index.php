<?php
/**
请完成一个函数，输入一个二叉树，该函数输出它的镜像。

例如输入：

     4
   /   \
  2     7
 / \   / \
1   3 6   9
镜像输出：

     4
   /   \
  7     2
 / \   / \
9   6 3   1

 

示例 1：

输入：root = [4,2,7,1,3,6,9]
输出：[4,7,2,9,6,3,1]

 */
/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     public $val = null;
 *     public $left = null;
 *     public $right = null;
 *     function __construct($value) { $this->val = $value; }
 * }
 */
class Solution {

    /**
     * @param TreeNode $root
     * @return TreeNode
     * 交换左右节点
     */
    function mirrorTree($root) {
        if($root == null) return ;
        $tmp = $root->left;
        $root->left = $root->right;
        $root->right = $tmp;
        $this->mirrorTree($root->right);
        $this->mirrorTree($root->left);
        return $root;
    }
}